package com.gamelibrary.gamelibraryapp.model;

public class Developer {

    private Long id;
    private String name;

    public Developer() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
